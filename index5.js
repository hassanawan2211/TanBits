// == by using this type operators 

let a = 2;

let b = 20;

console.log( a == b );



// this type of operator check only value
let A = 20;

let B = "20"

console.log(A == B);


// === used for checking both data type and value 


let num = 56;

let num1 = "56"

console.log(num === num1);


let Name = "Ali";
let last_name = "Ali"

console.log(Name === last_name);



// for not equal 


let num2 = 6;

let num3 = "5";

console.log(num2 != num3);

// for not equal value or nor equal type is used !== 


let num4 = "ali";

let num5 = "ali";

console.log(num4 !== num5);



